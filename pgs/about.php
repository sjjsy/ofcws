<h4 id="club">The Club</h4>
<p>
  Otaniemi Fight Club (OFC) is an association that operates under the Student
  Union of Aalto University. The club is run by a small group of students with
  a mutual interest: practicing martial arts.
</p>
<p>
  Otaniemi Fight Club was founded on the sixth of May 2010 with a clear
  purpose: <i>to enhance the opportunities of the students of Aalto University
  to practice martial arts</i>. This is the club's mission and what it pursues to
  accomplish in many levels -- not just by offering some square meters of
  tatami to train on, but also by providing some equipment, information and a
  forum for its members, and perhaps most importantly, the critical mass
  needed to accomplish things a lone fighter might not.
</p>
<p>
  The club organizes weekly sessions for wrestling and kickboxing. These make up
  the core of the club's event calendar. In addition to the regular sessions the
  club organizes excursions to other martial arts clubs or schools or the like
  to boost learning. To spice up, events unrelated to martial arts are also
  included from time to time such as sauna nights, co-organized chillouts with
  other associations and introductions to other fields of sports. See the
  <a href="/pgs/events.php">events page</a> for information about current
  plans.
</p>


<h4 id="board">The Board</h4>

<p>
  The club's board members are elected every year. However, we were unable to
  find a replacement for the club's chairman of the year 2015. Thus the club is
  currently headless and is thus unable to meet its goals!
</p>
<p>
  Therefore, we're looking for a new person to assume the role of the chairman
  of the club. There is very little mandatory work so in case you wan't to know
  about how small sports associations work or just would like to gather a group
  of friends to wrestle and train with, please contact f. ex. the chairman of
  2015 to discuss how he could help you start running the club.
</p>
<p>
  The club does not need to continue the way it has so far: So if you have ideas
  on what the club could do or what it shouldn't it would only be great.
  As assets, the club has some martial arts training equipment and cash to help
  you get started.
</p>

