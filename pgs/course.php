<h5>
  Equipment
</h5>

<p>
  Fully participating in full-contact exercises like boxing or MMA-sparring requires proper equipment. Use of insufficient or poor quality equipment can lead to injury.
</p>

<p>
  However, one can participate in at least about 70% of the course without any equipment due to the fact that the course has a lot of drills and exercises where there is no hard contact. In addition, the club will be able to sell or lend some equipment such as boxing gloves, hand wraps and mouth guards. No equipment is needed for the first session.
</p>

<p>
  In case you are interested to acquire equipment yourself, the priority order is: 1) Mouth guard, 2) Boxing (sparring, 16 oz) or MMA gloves (padded), 3) Jockstrap, 4) Shin guards, 5) Headgear.
</p>

