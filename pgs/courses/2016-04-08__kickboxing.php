<h4>
  Content
</h4>

<p>
  For a more detailed peek at the content of each session take a look at the
  "Content" column in
  <a href="https://docs.google.com/spreadsheets/d/1cPrAhgZTeTgRvP8RpFe5rzm8lKdpY87lQcpx1Hl6RV4/edit?usp=sharing">
    the sheet
  </a>
  we've been using to plan the course.
</p>

<p>
  In the muay thai trainings the focus will be in techniques relevant in
  kickboxing with <a href="https://en.wikipedia.org/wiki/K-1">K1</a> rules.
  So no standup grappling or throwing techniques will be exercised during
  these sessions. Join our Monday BJJ sessions in case you want to wrestle!
</p>

<p>
  We'll try to keep the Friday sessions a bit less intensive on the muscles
  and bones such that it shouldn't be too hard for you to get yourself to the
  muay thai trainings the day after.
</p>


