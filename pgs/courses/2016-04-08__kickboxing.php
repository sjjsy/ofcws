<h4>
  Locations
</h4>

<p>
  <b>Otahalli</b> is an Unisport gym at Otaranta 6, 02150 Espoo. We train
  at a sports hall named "Monitoimihalli" at "Heittopaikka".
  The tatami area is left of the entrance but you must walk almost to the
  far-left corner to spot it.
  In case you have a valid Unisport subscription, you can register for
  the session via the Unisport reader, otherwise just say you're going
  to attend "Vapaaottelu" to the receptionists. Finding the place and
  changing your clothes at the dressing rooms might take time, so
  please adjust your schedules accordingly.
</p>
<p>
  <b>Tatamisali</b> is a small hall with tatami owned by AYY at
  Jämeräntaival 5 A. The hall is immediately right from the entrance.
  Note that there are no seperate rooms for changing clothes and the only
  shower can be accessed only when the sauna happens to be open.
</p>


<h4>
  Objectives
</h4>

<p>
  Teach and train some basic kickboxing and muay thai techniques, introduce
  participants to light sparring, provide good physical exercise and have fun.
</p>

<p>
  The secondary objective is to grow the club's weekly training participant
  pool especially in the non-beginner segment. The club's board is also in
  need of fresh blood supply. :)
</p>


<h4>
  Instructors
</h4>

<p>
  Carin will lead the kickboxing sessions and Antti the muay thai sessions.
  Elias and Samuel will assist the two in some of the trainings.
  Read more about the club's instructors at our
  <a href="/pgs/guides.php">
    guides page.
  </a>
</p>


<h4>
  Requirements
</h4>

<p>
  In addition to a positive, social and sporty attitude it would be great if
  you would have some kickboxing gear of your own (boxing gloves and mouth,
  groin and shin guards).
  The club will only be able to loan a few pieces of equipment for the
  trainings.
  In terms of clothing a sturdy pair of shorts and a t-shirt will do.
  No socks on the tatami.
  Just remember to round your nails and lay an eye on our terms of
  participation
	<a href="http://bit.ly/1co4oyc">in English</a> or
	<a href="http://bit.ly/16YllRa">in Finnish</a>.
</p>


