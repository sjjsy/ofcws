<h3 id="facts">
  Facts
</h3>


<h4>
  Practicalities
</h4>

<p>
  Both benchmarking sessions will be identical and held in Otaniemi in or around Otahalli (Otaranta 6, 02150 Espoo, Finland).
</p>

<p>
  You can read more about the tests
	<a href="/pgs/fitness_test.php">
    here
  </a>
  but to summarize:
</p>

<ul>
  <li>
    The test consists of five subtests: one aerobic test and four anaerobic tests
  </li>
  <li>
    The aerobic test will be performed as a timed two mile (8x400m) run
  </li>
  <li>
    The anaerobic tests will be performed as follows
    <ul>
      <li>
        Standing Long Jump -- 3 trials, with only the longest counted
      </li>
      <li>
        Sit Up -- Number of successful repeats in 60 seconds. Feet held.
      </li>
      <li>
        Push Up -- Number of successful repeats in 60 seconds.
      </li>
      <li>
        Pull Up -- Number of successful repeats without a pause
      </li>
    </ul>
  </li>
  <li>
    Each test result is graded and the final "fitness grade" is the based 50% on the aerobic grade and 50% on the average of the anaerobic grades.
  </li>
</ul>

<p>
  Note: There's no need for you to participate in all tests if you're only interested to benchmark specific areas. For instance, if you prefer not to run or jump, you can skip the beginning. Check out the schedule below.
</p>


