<h2>A Basic Fitness Test</h2>

<p>
Largely based on
  "Puolustusvoimien kuntotestaajan käsikirja 2011"
by the Finnish Military.
It can be downloaded from
  <a href="http://www.resul.fi/files/805/kuntotestaajan_kasikirja_2011.pdf">
    here.
  </a>
</p>

<h3>Overview</h3>
<p>
The test score or the (Overall) <strong>Fitness Score</strong> is the averge of two subscores: the <strong>Aerobic Fitness Score</strong>, and the <strong>Muscle Fitness Score</strong>.<br>
</p>

<p>
The Aerobic Fitness Score is ment to reflect the subject's maximal aerobic capacity while the Muscle Fitness Score is designed to reflect overall strenght and anaerobic fitness.<br>
</p>

<p>
Both subscores consist of one or more sub tests. Below is a listing of each sub test together with the capacity they are targeting:<br>
</p>

<h4>Aerobic Fitness Score</h4>
<p>
<ul>
<li>A1) <strong>Cooper's Test</strong> — Maximal aerobic capacity</li>
</ul>
</p>

<h4>Muscle Fitness Score</h4>
<p>
<ul>
<li>M1) <strong>Standing Long Jump</strong> — Explosive lift capacity of the lower body</li>
<li>M2) <strong>Sit Up</strong> — Dynamic fitness of the abdominal mucles and the hip flexors</li>
<li>M3) <strong>Push Up</strong> — Dynamic strength and fitness of the muscles of the shoulder area and arms</li>
<li>M4) <strong>Pull Up</strong> — Dynamic strenght of the muscles of the upper back, torso and arms</li>
</ul>
</p>

<h4>Note</h4>
<p>
<ol type="1" start="1">
<li>There should be a warmup of at least 15 minutes before any testing and a pause of 5 to 10 minutes between each sub test.</li>
<li>The Cooper's Test should not be done on the same day as the rest of the tests. The three days preceding the tests should be free of physical stress.</li>
<li>Testing must not be performed if the subject is ill, in a subnormal physical condition or is subject to other health risks (f. ex. has a cardiac pacemaker, suffers from severe asthma).</li>
</ol>
</p>

<h3>Scoring</h3>
<p>
Each sub test result is converted to a grade (5, 4, 3, 2, 1 and 0) using a table that matches the subject's age and gender.<br>
</p>

<p>
To form the Muscle Fitness Score each strength test grade is combined (added together and divided by the number of tests).<br>
</p>

<p>
The Fitness Score can be expressed with an adjective:<br>
<ul>
<li>0.00–0.99 Weak</li>
<li>1.00–1.49 Poor</li>
<li>1.50–2.49 Satisfactory</li>
<li>2.50–3.49 Good</li>
<li>3.50–4.49 Very good</li>
<li>4.50–5.00 Excellent</li>
</ul>
</p>

