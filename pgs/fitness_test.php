<h3>Sub Test Descriptions</h3>

<h4>A1) Cooper's Test / Cooper-testi</h4>
<p>
<ul>
<li>Purpose: To measure the subject's long distance <a href="./Maximal_Oxygen_Consumption.html" title="maximal oxygen consumption" class="page">maximal oxygen consumption</a>. The test was designed by Kenneth H. Cooper in 1968 for US military. Everything above 3 km is rated "long distance" — which means the runner will predominately use his "red", slow oxidative muscle cells.</li>
<li>Execution: Running as far a distance within 12 minutes on a good, flat running surface (steady, maximal pace).</li>
<li>Score: The distance covered.</li>
</ul>
</p>

<h4>M1) Standing Long Jump / Vauhditon pituushyppy</h4>
<p>
<ul>
<li>Purpose: To measure the explosive strength generation ability of the lower body.</li>
<li>Execution: Jumping the maximal distance past a line, with the feet together. The jump is done on a special distance jumping spot with no speeding, legs slightly apart, and the toes behind the line.</li>
<li>Score: The distance covered.</li>
</ul>
</p>

<h4>M2) Sit Up / Istumaannousu</h4>
<p>
<ul>
<li>Purpose: To measure the strenght and fitness of the abdominal muscles involved in performing the sit up.</li>
<li>Execution: Alternating from the <em>low</em> position to the <em>high</em> position as many times as possible within 60 seconds without breaking the form.</li>
<ul>
<li><em>Overall</em>: Lying on the back, knee angle at 90°, feet held on the ground f. ex. by an assistant, both palms (sormet ristissä) on the back of the head, elbows pointing towards the knees. The form must be constant, the buttocks and feet at the same spot: Only the torso angle alternates.</li>
<li><em>Low</em>: Body lowered, lower scapula touching the ground.</li>
<li><em>High</em>: Body lifted, elbows touching the knees.</li>
<li>Mistakes: Moving the palms off the rear of the head; Swinging the elbows and the head to gain momentum; Not achieving the <em>low</em> and <em>high</em> positions perfectly.</li>
</ul>
<li>Score: The number of successful repeats in 60 seconds. (Theoretical maximum is about 90 repeats per minute)</li>
</ul>
</p>

<h4>M3) Push Up / Etunojapunnerrus</h4>
<p>
<ul>
<li>Purpose: To measure the strength and fitness of the shoulder area and arms as well as the static performance of supporting muscles.</li>
<li>Execution: Alternating from the <em>low</em> position to the <em>high</em> position as many times as possible within 60 seconds without breaking the form.</li>
<ul>
<li><em>Overall</em>: Hands straight below the shoulders, fingers pointing forward, legs completely straight and the feet at most at the width of the pelvis, body straight (thigh-pelvis-torso and the neck angles both between 160-180°) with only the toes and palms supporting the body weight off the ground. The form must be constant: Only the arms move.</li>
<li><em>Low</em>: Body lowered, biceps horizontally, elbows at a 70-90° angle.</li>
<li><em>High</em>: Body lifted, elbows completely extended (170-180°).</li>
<li>Mistakes: Breaking the form (Pelvis angle; resting); Not achieving the <em>low</em> and <em>high</em> positions perfectly.</li>
</ul>
<li>Score: The number of successful repeats in 60 seconds. (Theoretical maximum is about 90 repeats per minute)</li>
</ul>
</p>

<h4>M4) Pull Up / Leuanveto</h4>
<p>
<ul>
<li>Purpose: To measure the strength and fitness of a variety of muscles (especially the <a href="http://en.wikipedia.org/wiki/Latissimus_dorsi" title="latissimus dorsi" class="http">latissimus dorsi</a>) involved in performing the pull up movement.</li>
<li>Execution: Alternating from the <em>low</em> position to the <em>high</em> position as many times as possible without releasing the grip or touching anything other than the bar.</li>
<ul>
<li><em>Overall</em>: Body weight supported only by the bar which is held with both hands in a supinated grip.</li>
<li><em>Low</em>: Hanging low, elbows completely extended (170-180°).</li>
<li><em>High</em>: Body lifted high, jaw over the bar with the head in horizontal angle.</li>
<li>Mistakes: Swinging or moving the body in some way to gain momentum; Not achieving the <em>low</em> and <em>high</em> positions perfectly.</li>
</ul>
<li>Score: The number of successful repeats.</li>
</ul>
</p>


