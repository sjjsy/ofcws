	membership fee.
</p>


<h4 id="whocan">Who Can Join?</h4>

<p>
	The club is primarily for the students of Aalto University and we might limit the
	number of outsiders due to the constraints set by our resources. However,
	even non-Aaltoers can be accepted as half-members, after which they have
	the right to take part in our activities.
</p>


<h4 id="costs">What Does It Cost?</h4>

<p>
	The membership fee for 2014 is 20 euros. It is also possible to join for
	only the spring or fall term. The fee for such a term is 10 euros.
	Some special events might have a small additional fee but in these cases
	the amount is always informed clearly beforehand. The weekly core sessions
	are free.
</p>


