<h2 id="joinus">Join Us</h2>

<h4 id="why">Why?</h4>

<p>
  The club exists to serve its members.
  Only members are allowed to fully participate in the club's activities and
  benefit from what the club can offer.
  Non-members are only allowed to visit a few weekly trainings in order to get
  familiar with the club before committing the membership fee.
</p>

The club offers:
<ol>
  <li>
    Like-minded fellow members
  </li>
  <li>
    Training opportunities, facilities and equipment
  </li>
  <li>
    Training instruction
  </li>
  <li>
    Social pressure and help to keep you fit :)
  </li>
</ol>

<p>
  Some courses and special events might have a small additional fee but in
  these cases the amount is always informed clearly beforehand.
  The weekly core sessions are always free for members.
</p>


<h4 id="whocan">Who Can Join?</h4>

<p>
  The club is primarily by and for the students of Aalto University and we
  might need to limit the number of outsiders due to our constrained
  resources.
  However, even non-Aaltoers can be accepted as half-members, after
  which they have the right to take part in our activities.
</p>


<h4 id="costs">What Does It Cost?</h4>

<p>
  The membership fee for 2015 is 16 euros. It is also possible to join for
  only the spring or fall term. The fee for such a term is 10 euros.
</p>


