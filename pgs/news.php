    <h3>Syyskokouksen esityslista</h3>

    <p>Syyskokouksen aloitusta aikaistettiin. Kokouksen aloitusajankohta on nyt 29.11.2013 17:30. Kokouksen esityslista on seuraava:</p>

    <ol>
    <li>Kokouksen avaus</li>
    <li>Kokouksen järjestäytyminen</li>
    <li>Kokouksen laillisuus ja päätösvaltaisuus</li>
    <li>Kokouksen työjärjestys</li>
    <li>Hallituksen puheenjohtajan valinta seuraavaksi toimikaudeksi</li>
    <li>Hallituksen muiden jäsenten valinta seuraavaksi toimikaudeksi</li>
    <li>Yhdistyksen toimihenkilöiden valinta seuraavaksi toimikaudeksi</li>
    <li>Toiminnantarkastajien valinta seuraavaksi toimikaudeksi</li>
    <li>Muutoksia yhdistyksen sääntöihin</li>
    <li>Rekisteröityminen ry:ksi</li>
    <li>Vastuu käteiskassasta sen avaimien haltijoille</li>
    <li>Työpanoskorvaukset</li>
    <li>Muuta esille tulevaa</li>
    <li>Kokouksen päättäminen</li>
    </ol>

    <p>Lisätietoa:</p>

    <p>Hallitus haluaa siis sääntömuutoksien lisäksi äänestyttää rekisteröitymisen ry:ksi. Huom. Käynnistimme prosessin selvittääksemme AYY:n järjestösektorin kanssa sääntöpäivityksemme järkevyyttä vasta äskettäin ja voi olla, ettei heidän palaute ja mahdolliset korjausehdotukset ehdi kokoukseen mennessä, joten saattaa olla, ettei sääntöjen osalta tehdä virallisia päätöksiä. Keskustelua niistä on kuitenkin varmasti tiedossa.</p>

    <p>Myös "Työpanoskorvaus" asia saattaa vielä muuttaa muotoaan.</p>

    <p>Katsokaa alustava kokouspöytäkirja. Sieltä huomaatte, että hallitukseen ja toimihenkilöiksi on tarjolla useita mielenkiintoisia tehtäviä. The more we are, the more we can do. Tulkaa kaikki rohkeasti vaalikokoukseen, niin saatte miehekkään purilaisenne lisäksi komean tittelin!</p>

    <p>Muistutan vielä AYY:n 26.11. järjestämästä yhdistyskoulutuksesta; Suosittelen järjestötoimintaa teille kaikille ja tämä tietopaketti varmasti helpottaa taipaleen aloitusta ja vähentää askarruttavien kysymysten määrää. illmoittautumaan sinne ja tulkaa mukaan!</p>

