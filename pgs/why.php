<h2>Why Mixed Martial Arts?</h2>




<p>
  Why invest your time and energy in MMA? An important question!
  There are many answers (try and google for some) but here's
  three reasons that came to <em>my</em> mind:
  <br/>
  <em>In my opinion</em>...
</p>


<ol>
  <li>
    <p>
	    <strong>
	      MMA is the most natural, balanced and effective way
	      to strengthen a human body:
      </strong>
    </p>

    <p>
      Please watch some of the embedded videos below and then agree with me: It's
	    <strong>
        an unparalled way to stress a human body, brain and braw:
      </strong>
      <ul>
        <li>
          <p>
            <strong>
              Body.
            </strong>
            MMA stresses your bones, joints, neck, feet, hands, ankles, and almost every other part
            of your body you could think of and we all know that frequent moderate stress triggers
            the body to strengthen itself; Wrestlers have <i>grown themselves robust</i>.
            Streching and reach exercises and things that dancers and acrobats alike do also form
            an integral part in developing MMA-capable -- versatile -- physique.
          </p>
        </li>
        <li>
          <p>
            <strong>
              Brain.
            </strong>
            MMA isn't a piece of cake for the brain either
            -- getting better in it requires you to learn
            not just different techniques and moves but also
            about practical everyday physics, about yourself, about other people, about movement,
            about limits and about many other things.
            It develops your nervous system in many many ways:
            You'll develop -- just to name a few -- coordination, dexterity, agility & speed
            -- none of which is developed by f. ex. plain long-distance running or regular weight-lifting.
            Weight-lifting might give you the braw, but leaves you without the brain to use it -- almost for any purpose.
          </p>
        </li>
        <li>
          <p>
            <strong>
              Braw.
            </strong>
            MMA requires strength, stamina and raw power!
            You'll learn this clearest when you try to fight us.
            What's best in MMA is that it boosts your braw in the most natural, balanced and useful ways.
            One of the most important benefits of training MMA is that the trainings constantly give you
            feedback on your current fit and push you to do your supportive workouts.
            <em>Yes</em>, most MM-artists stress themselves with an additional combination of
            other activities.
            Personally I'm in the process of coupling MMA with regular body weight
            and gym workouts, and occasional triathlon and dance training
            to form <em>my</em> recipe for fitness.
          </p>
        </li>
      </ul>
    </p>
  </li>


  <li>
    <p>
	    <strong>
	      MMA makes a good friend
      </strong>
    </p>
    
    <p>
      You know the saying that men grow old because they stop X, and not conversely.
      Well MMA is a sport that forces you to keep yourself able.
      Why? Because it offers you
      <ul>
        <li>
          <p>
            <strong>
              a healthy dose of competition.
            </strong>
            Never lose the urge to become faster, stronger & better.
            If you lose it, the opposite will happen.
            Competition doesn't have to taste like blood but brothers who don't care who's better
            get nowhere. Competition is in the essence of us. And for a reason.
            MMA teaches you to relish it.
          </p>

        <li>
          <p>
            <strong>
              good friends.
            </strong>
            As a social sport (unlike, say, swimming) you'll get friends with
            a similar mindset and you'll push each other farther, faster and stronger.
            They'll also notice you if you slow down or start to fade out.
            Your MMA friends can prove to be valuable beyond measure.
            MMA teaches you to relish them.
          </p>
        </li>
        <li>
          <p>
            <strong>
              animal like feelings of pain and achievement.
            </strong>
            It's healthy to be reminded of how pain feels and of the fact
            that your body has limits. It's healthy to feel the temptation to submit and quit
            and the feelings of exhaustion, of powerlessness, of submission and of defeat.
            It's equally healthy to feel feelings of power, achievement, success and victory.
            These things keep your animal (male) brain sane and relax, and they relieve stress.
            MMA teaches you to seek these feelings, learn from them, and relish them.
          </p>
        </li>
      </ul>
    </p>
    <p>
      Regular determined training also helps you keep good sleep and eating habits which is very important.
    </p>
  </li>


  <li>
    <p>
	    <strong>
	      MMA training and experience is beneficial in surprising ways
      </strong>
    </p>
    
    <p>
      You already know that the "martial" in MMA reminds of war and real threats
      and while MMA provides undoubtedly perhaps the most effective and practical
      means to varying potential situations there are also other reasons why
      experience in MMA can be useful:
      <ul>
        <li>
          <p>
            <strong>
              Accidents.
            </strong>
            You'll better cope with various accidents like tripping on ice and crashing
            with a bicycle are a lesser threat when you've trained yourself to
            avoid, evade, block and resist impacts every week for years.
            You know how to fall. You know when and how to protect what's most important.
          </p>
        </li>
        <li>
          <p>
            <strong>
              Attitudes.
            </strong>
            You'll develop a healthy attitude towards physical contact and violence
            and learn to control yourself and your agression.
            You'll also learn empathy and trust and most of all: discipline and willpower.
            These are things that shape who you are and who you'll become.
          </p>
        </li>
        <li>
          <p>
            <strong>
              Confidence.
            </strong>
            You'll be more confident where ever you go while simultaneously knowing
            your limits better than most. Confidence and self-awareness are advantages
            in many fronts of life. Why should one seperate one's MMA experience from
            one's business career?
          </p>
        </li>
      </ul>
    </p>
  </li>
</ol>




